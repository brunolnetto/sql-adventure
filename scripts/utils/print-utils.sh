# Progress indicator
print_progress() {
    local current=$1
    local total=$2
    local width=50
    local percentage=$((current * 100 / total))
    local completed=$((width * current / total))
    local remaining=$((width - completed))
    
    printf "\r["
    printf "%${completed}s" | tr ' ' 'â–ˆ'
    printf "%${remaining}s" | tr ' ' 'â–‘'
    printf "] %d%%" $percentage
    
    if [ "$current" -eq "$total" ]; then
        echo
    fi
}

